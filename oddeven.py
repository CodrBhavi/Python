# Python Program to check weather a number is odd or even
# Taking an Integer Input
num = int(input("Enter a Number: "))
# If Entered Number is divisible with 2 then, the number is Odd Number
if num%2==0:
	print("This is Odd Number")
# If Entered Number is not divisible with 2 then, the number is Even Number
else:
	print("This is Even Number")
