# Swapping Numbers Program in Python
# Taking Input from user
num1 = eval(input("Enter a Number for num1: "))
num2 = eval(input("Enter a Number for num2: "))
print("Before Swapping.........")
print("num1 is ",num1)
print("num2 is ",num2)
# Swapping Logic
num1,num2 = num2,num1
# After Swapping Numbers
print("After Swapping.........")
print("num1 is ",num1)
print("num2 is ",num2)
# End Program